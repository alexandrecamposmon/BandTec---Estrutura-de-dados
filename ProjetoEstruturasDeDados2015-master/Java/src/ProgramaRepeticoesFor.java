
public class ProgramaRepeticoesFor {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		for(int x=0; x < 3; x++)
		{
			System.out.println("Dolce Gabbana");
		}
		
		int z = 10;
		for (;z>0;z--)
		{
			System.out.println("Axe");
		}
		
		for(int kk=0;;kk++)
		{
			System.out.println("One million");
			if(kk<3){
				break;
			}
		}
		
		
		
	}

}
