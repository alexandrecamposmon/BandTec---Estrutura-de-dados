// como as demais variáveis, 
// vetores são de tipagem dinâmica em JS
var frutas = ["amora","banana","caqui","damasco"];
var pesos = [71, 92.5, 105.2];

// para recuperar um valor:
console.log("1º valor de frutas: "+frutas[0]);
console.log("3º valor de pesos: "+pesos[2]);

// pode-se exibir o vetor inteiro
console.log(frutas);

// pode-se aumentar um vetor! 
frutas[4]="morango";
console.log(frutas);



